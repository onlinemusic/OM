package com.ssm.dto.ranklisttosongs;

public class RanklistToSongs {

	private int id;
	
	private int rid;//排行榜ID
	
	private Long sid;//歌曲ID

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getRid() {
		return rid;
	}

	public void setRid(int rid) {
		this.rid = rid;
	}

	public Long getSid() {
		return sid;
	}

	public void setSid(Long sid) {
		this.sid = sid;
	}
	
	
}
